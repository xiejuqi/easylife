CREATE TABLE `PERMISSION` (
	`ID` INT(20) NOT NULL AUTO_INCREMENT,
	`NAME` VARCHAR(20) NULL COMMENT '权限名称',
	`PARENT_ID` INT(20) NULL COMMENT '父权限ID',
	`DESCRIPTION` VARCHAR(50) NULL COMMENT '权限描述',
	`HREF` VARCHAR(100) NULL COMMENT '权限链接地址',
	`VISIBLE` VARCHAR(1) NOT NULL COMMENT '该权限是可显示',
	`TYPE` VARCHAR(20) NOT NULL COMMENT '权限类型[菜单|按钮|页面]',
	`ICON` VARCHAR(20) NULL COMMENT '权限图标',
	`CSS_CLASS` VARCHAR(20) NULL COMMENT '权限css样式类名',  
	`PERMISSION` VARCHAR(50) NULL COMMENT '权限内容字符串',
	`INSERT_BY` VARCHAR(50) NULL  COMMENT '创建人',
	`INSERT_TIME` TIMESTAMP NOT NULL DEFAULT 0 COMMENT '创建时间',
	`UPDATE_BY` VARCHAR(50) NULL COMMENT '更新人',
	`UPDATE_TIME` TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP COMMENT '更新时间',
	PRIMARY KEY (`ID`)
)
COMMENT='易生活权限表'
COLLATE='UTF8_GENERAL_CI'
ENGINE=INNODB
;

