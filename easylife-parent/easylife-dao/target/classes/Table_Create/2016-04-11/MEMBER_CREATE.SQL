CREATE TABLE `MEMBER` (
	`ID` INT(20) NOT NULL AUTO_INCREMENT,
	`ACCOUNT` VARCHAR(20) NOT NULL COMMENT '会员帐户',
	`NAME` VARCHAR(20) NOT NULL COMMENT '会员姓名',
	`PASSWORD` VARCHAR(30) NOT NULL COMMENT '会员密码',
	`PID` VARCHAR(20) NOT NULL COMMENT '身份证号',
	`EMAIL` VARCHAR(30)  NULL COMMENT '电子邮箱',
	`TEL` VARCHAR(30)  NULL COMMENT '手机号码',
	`SEX` VARCHAR(1) NOT NULL COMMENT '性别,M为男,W为女',
	`ACTIVE` VARCHAR(1) NOT NULL COMMENT 'Y有效,N失效',
	`ACCOUNT_STATUS` VARCHAR(20) NOT NULL COMMENT '账户状态[ACTIVE|LOCK]',
	`INSERT_BY` VARCHAR(50) NULL  COMMENT '创建人',
	`INSERT_TIME` TIMESTAMP NOT NULL DEFAULT 0 COMMENT '创建时间',
	`UPDATE_BY` VARCHAR(50) NULL COMMENT '更新人',
	`UPDATE_TIME` TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP COMMENT '更新时间',
	PRIMARY KEY (`ID`)
)
COMMENT='易生活会员表'
COLLATE='UTF8_GENERAL_CI'
ENGINE=INNODB
;

